# Stack = LIFO (Last In First Out)

stack = []

# push
stack.append(10)
stack.append(20)
stack.append(30)

print("Stack after push:", stack)

# pop
stack.pop()

print("Stack after pop:", stack)

# When to use Stack:
# - Valid Parentheses
# - Undo operations
# - Expression evaluation

# Time Complexity:
# push -> O(1)
# pop  -> O(1)

print("Stack Revision Completed ✅")